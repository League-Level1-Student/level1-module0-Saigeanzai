		static void robot() {
			Robot rob = new Robot();
			for(int i=0; i<3; i++) {
				
				rob.miniaturize();
				rob.penDown();
				rob.setSpeed(100);
			    rob.move(10);
			    rob.turn(-90);
			    rob.move(100);
			    rob.turn(90);
			    rob.move(20);
			    rob.turn(90);
			    rob.move(100);
			    rob.turn(-90);
			}
			
			
			
			
			
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
