public class Houses {
		
	public void run() {
		// Check the recipe to find out what code to put here
		Robot rob = new Robot();
		for (int i=0; i<5; i++) {
			rob.miniaturize();
			rob.setSpeed(100);
			rob.setPenColor(250,0,0);
			rob.penDown();
			rob.move(100);
			rob.turn(50);
			
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
